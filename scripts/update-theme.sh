#!/bin/bash

source ~/dotfiles/theme.sh

HYPR_CONF="$HOME/dotfiles/config/hypr/colors.conf"
WAYBAR_CONF="$HOME/dotfiles/config/waybar/colors.css"
SWAYNC_CONF="$HOME/dotfiles/swaync/colors.css"
KITTY_CONF="$HOME/dotfiles/config/kitty/theme.conf"

ALPHA_DEC=$(printf "%d" "0x${OPACITY}")
ALPHA_FLOAT=$(LC_NUMERIC=C awk "BEGIN {printf \"%.2f\", ${ALPHA_DEC}/255}")

# ---------------------------------------------------------
# GTK
# ---------------------------------------------------------
# gsettings set org.gnome.desktop.interface gtk-theme "$GTK_THEME"
# gsettings set org.gnome.desktop.interface icon-theme "$ICON_THEME"
# #gsettings set org.gnome.desktop.interface font-name "$FONT_NAME $FONT_SIZE_SMALL"
# echo "Updated GTK"

# ---------------------------------------------------------
# KITTY
# ---------------------------------------------------------
cat > "$KITTY_CONF" <<EOF
# Generated by update-colors.sh

background_opacity $ALPHA_FLOAT
window_padding_width 10
hide_window_decorations yes

font_family      $FONT_NAME
font_size        $FONT_SIZE

background #$COLOR_BG
foreground #$COLOR_FG

selection_background #$COLOR_ACCENT
selection_foreground #$COLOR_BG

url_color #$COLOR_ACCENT
cursor    #$COLOR_ACCENT
cursor_text_color #$COLOR_BG

color0 #$COLOR_ALT
color8 #$COLOR_ALT

color1 #$COLOR_URGENT
color9 #$COLOR_URGENT

color2 #$COLOR_SUCCESS
color10 #$COLOR_SUCCESS

color3 #$COLOR_ACCENT
color11 #$COLOR_ACCENT

color4 #$COLOR_ACCENT
color12 #$COLOR_ACCENT

color5 #$COLOR_URGENT
color13 #$COLOR_URGENT

color6 #$COLOR_ACCENT
color14 #$COLOR_ACCENT

color7 #$COLOR_FG
color15 #$COLOR_FG
EOF

echo "Updated kitty"

killall -SIGUSR1 kitty 2>/dev/null

echo "Theme updated!"
